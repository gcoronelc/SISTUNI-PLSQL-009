

CREATE OR REPLACE PROCEDURE SCOTT.SP_SUMA(
  P_N1   IN  NUMBER,
  P_N2   IN  NUMBER,
  P_SUMA OUT NOCOPY NUMBER
)
IS
BEGIN
  -- PROCESO
  P_SUMA := P_N1 + P_N2;
  RAISE_APPLICATION_ERROR(-20000, 'Jejejeje, AHORA si se copio el resultado');
END;
/


DECLARE
  V_SUMA NUMBER := 20;
BEGIN
  DBMS_OUTPUT.PUT_LINE( 'SUMA: ' || V_SUMA );
  SCOTT.SP_SUMA( 5, 6, V_SUMA );
  DBMS_OUTPUT.PUT_LINE( 'SUMA: ' || V_SUMA );
EXCEPTION
  WHEN OTHERS THEN
    DBMS_OUTPUT.PUT_LINE( 'ERROR: ' || SQLERRM );
    DBMS_OUTPUT.PUT_LINE( 'SUMA: ' || V_SUMA );
END;
/









