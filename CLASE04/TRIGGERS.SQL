
CREATE OR REPLACE TRIGGER SCOTT.TR_TEST_EMP
AFTER INSERT OR DELETE OR UPDATE ON SCOTT.EMP
FOR EACH ROW
BEGIN
  IF INSERTING THEN
     DBMS_OUTPUT.PUT_LINE('NUEVO EMPLEADO.');
  ELSIF UPDATING THEN
     DBMS_OUTPUT.PUT_LINE('EMPLEADO MODIFICADO.');
  ELSIF DELETING THEN
     DBMS_OUTPUT.PUT_LINE('EMPLEADO ELIMINADO.');
  END IF;
END;
/

